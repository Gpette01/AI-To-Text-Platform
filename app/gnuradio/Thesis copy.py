def argument_parser():
    parser = ArgumentParser()
    parser.add_argument(
        "--fc", dest="fc", type=eng_float, default=eng_notation.num_to_str(float(446.031e6)),
        help="Set fc [default=%(default)r]")
    parser.add_argument(
        "--file", dest="file", type=str, default='/home/ubuntulaptop/Desktop/file.wav',
        help="Set file [default=%(default)r]")
    return parser

def changeFrequency(tb):
    # Create a queue to share data between threads
    data_queue = Queue()
    # Start the receiver in a separate thread
    receiver_thread = threading.Thread(target=fc_receiver, args=(data_queue,), daemon=True)
    receiver_thread.start()
    while True:
        fc = data_queue.get()    
        if fc is not None:
            tb.set_fc(fc)
        time.sleep(1)

def main(top_block_cls=Thesis, options=None):
    if options is None:
        options = argument_parser().parse_args()

    if StrictVersion("4.5.0") <= StrictVersion(Qt.qVersion()) < StrictVersion("5.0.0"):
        style = gr.prefs().get_string('qtgui', 'style', 'raster')
        Qt.QApplication.setGraphicsSystem(style)
    qapp = Qt.QApplication(sys.argv)

    tb = top_block_cls(fc=options.fc, file=options.file)
    # tb.set_fc(102.3e6)
    # print(f"FC IS: {tb.get_fc()}")

    fcThread = threading.Thread(target=changeFrequency, args=(tb,))
    fcThread.daemon = True  # Set thread as daemon so it exits when main program exits
    fcThread.start()
    
    tb.start()

    tb.show()

    def sig_handler(sig=None, frame=None):
        tb.stop()
        tb.wait()

        Qt.QApplication.quit()

    signal.signal(signal.SIGINT, sig_handler)
    signal.signal(signal.SIGTERM, sig_handler)

    timer = Qt.QTimer()
    timer.start(500)
    timer.timeout.connect(lambda: None)

    qapp.exec_()
    


    
    

if __name__ == '__main__':
    main()
